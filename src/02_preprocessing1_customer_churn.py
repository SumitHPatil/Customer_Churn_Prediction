import pandas as pd
import numpy as np
from sklearn.preprocessing import LabelEncoder
from imblearn.over_sampling import SMOTE
import pickle

"""**Loading the dataset**"""

df =pd.read_csv('/content/Cleaned_Customer-Churn.csv')

df.head()

"""**Data preprocessing**"""

#converting categorical col to numerical using label encoding
object_col = df.select_dtypes(include=['object']).columns.to_list()
print(object_col)

#initialize the dictionary to store encoders
encoders = {}

#applying label encoders on categorical features
for column in object_col:
  encoder = LabelEncoder()
  df[column] = LabelEncoder().fit_transform(df[column])
  encoders[column] = LabelEncoder()

  #saving to pickle file
  with open('encoders.pkl', 'wb') as f:
    pickle.dump(encoder, f)

